package Class9Java;

import java.util.Scanner;

public class forLoopSum {
    public static void main ( String[] args ) {


        Scanner scanner = new Scanner(System.in);
        System.out.println("Enter a number: ");
        int x = scanner.nextInt();

        int sum=0;
        for (int i = x; i >x ; i--) {
            sum=sum+x;



        } System.out.println(sum);

    }

}
