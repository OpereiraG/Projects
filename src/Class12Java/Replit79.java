package Class12Java;

public class Replit79 {

        public static void main(String[] args) {

            double [][] numbers = {{1.5,1.8,4.5},{4,3,8.8}};

            for(int i =0; i<numbers.length; i++){
                for(int j=0;j<numbers[i].length;j++){
                    System.out.print(numbers[i][j]+" ");
                }
                System.out.println();
            }

        }
    }

