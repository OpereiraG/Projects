package Replits;

public class Replit116 {

    boolean evenOrOdd (int num1,int num2){
        if (num1%2==0 && num2%2==0){
            return true;
        }else {
            return false;
        }
    }

    public static void main(String[] args) {
        Replit116 r = new Replit116();
        System.out.println(r.evenOrOdd(6,6));
    }

}


