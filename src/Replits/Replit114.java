package Replits;

public class Replit114 {


    int addition(int num1,int num2){

        return (num1+num2);
    }
    int multiplication(int num1,int num2){

        return (num1*num2);
    }
    int subtraction(int num1,int num2){

        return (num1-num2);
    }
    public static void main(String[] args) {

        Replit114 n = new Replit114();
        System.out.println("Addition "+n.addition(15,15));
        System.out.println("Multiplication "+n.multiplication(3,10));
        System.out.println("Subtraction "+n.subtraction(30,10));


    }
}

