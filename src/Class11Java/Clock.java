package Class11Java;

public class Clock {
    public static void main ( String[] args ) {

        for (int i = 0; i <= 2; i++) {
            for (int j = 0; j < 4; j++) {
                for (int k = 0; k <= 5; k++) {
                    for (int l = 0; l <= 9; l++) {
                        for (int m = 0; m <= 5; m++) {
                            for (int n = 0; n <= 9; n++) {
                                System.out.println(i + "" + j + ":" + k + "" + l + ":" + m + "" + n);

                            }


                        }

                    }

                }
            }
        }
    }
}